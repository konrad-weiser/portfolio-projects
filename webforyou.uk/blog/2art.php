				<h2>What are cookies?</h2>
				<p>Cookies are small text files that a website stores on your computer or mobile device when you visit the site. These files contain information about your browsing activity, such as your preferences, login information, and other details about how you use the website.</p>
				<p>Websites use cookies to provide a better user experience by remembering your preferences and personalizing content to your interests.</p>
				<img width="596" height="294"alt="cookies" src="2image1.webp">
				<h2>Types of cookies</h2>
				<p>There are two main types of cookies: session cookies and persistent cookies. Session cookies are temporary and are erased when you close your web browser. They are used to maintain your user session, such as keeping you logged in to a website or remembering items in your shopping cart.</p>
				<p>Persistent cookies remain on your device until they expire or are manually deleted. They can be used for a variety of purposes, such as remembering your preferences across multiple sessions or tracking your browsing activity for analytics purposes.</p>
				<img width="596" height="183"alt="private, no public right of way" src="2image2.webp">
				<h2>Privacy and security concerns</h2>
				<p>Cookies can be useful for both users and website owners, but they can also raise concerns about privacy and security. Some people worry that cookies could be used to track their browsing activity or collect personal information without their consent. To address these concerns, many websites have implemented privacy policies that disclose how cookies are used and offer users options to control or delete cookies.</p>
				<h2>How do cookies work?</h2>
				<p>Cookies work by storing information about your browsing activity on your device. When you visit a website, the website sends a cookie to your web browser, which then stores the cookie on your device. The next time you visit the website, your web browser sends the cookie back to the website, allowing the website to remember your preferences and personalize content to your interests.</p>
